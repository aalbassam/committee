package sa.gov.sfd.permission.core;

public class EmployeeEntity {
    private EmployeeNID empNid;
    private EmployeeName empName;

    public EmployeeNID getEmpNid() {
        return empNid;
    }

    public void setEmpNid(EmployeeNID empNid) {
        this.empNid = empNid;
    }

    public EmployeeName getEmpName() {
        return empName;
    }

    public void setEmpName(EmployeeName empName) {
        this.empName = empName;
    }

    
}